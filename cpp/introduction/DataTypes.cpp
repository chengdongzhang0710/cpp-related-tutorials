#include <iostream>
using namespace std;

int main()
{
	// sizeof的用法
	cout << "The RAM size of type short is: " << sizeof(short) << endl;
	cout << "The RAM size of type int is: " << sizeof(int) << endl;
	cout << "The RAM size of type long is: " << sizeof(long) << endl;
	cout << "The RAM size of type long long is: " << sizeof(long long) << endl;

	system("pause");
	return 0;
}
